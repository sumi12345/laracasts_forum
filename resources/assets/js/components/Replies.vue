<template>
    <div>
        <div v-for="(reply, index) in items" :key="reply.id">
            <reply :data="reply" @deleted="removeElement(index)"></reply>
        </div>
    </div>
</template>

<script>

    import Reply from './Reply.vue';

    export default {
        props: ['data'],

        components: {
            reply: Reply
        },

        data() {
            return {
                items: this.data
            }
        },

        methods: {
            // remove 是保留字, remove 方法改为 removeElement 方法
            removeElement(index) {
                this.items.splice(index, 1);

                this.$emit('removed');
            }
        }
    }
</script>